package com.bonusGo.Bonus.Go.model;

public class UsuarioLoginRequest {

    private String correo;
    private String pass;

    // Getters y Setters
    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
